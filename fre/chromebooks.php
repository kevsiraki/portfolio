        <h2>Chromebook Basics:</h2>
        <p>Here are some fundamental aspects of using Chromebooks in GUSD:</p>
        <ul>
            <li>
                <strong>K-3rd Grade Students:</strong> Students in these grades can log in using a Clever QR code badge.
            </li>
            <li>
                <strong>4th-6th Grade Students:</strong> These students log in using their Google account credentials.
                They
                do not need to enter "@stu.gusd.net" as it is autofilled.
            </li>
            <li>
                <strong>Staff Members:</strong> Staff members logging in with GUSD accounts need to type the full
                "@gusd.net".
            </li>
        </ul>
        <h3>Updating Chromebook Software:</h3>
        <ol>
            <li>
                <p>Connect your Chromebook to a Wi-Fi network.</p>
            </li>
            <li>
                <p>Click on the clock in the bottom-right corner of the screen to open the menu.</p>
            </li>
            <li>
                <p>Select the gear icon to open "Settings".</p>
            </li>
            <li>
                <p>In the "Settings" menu, scroll down and click on "About Chrome OS" in the left sidebar.</p>
            </li>
            <li>
                <p>Click on "Check for updates".</p>
            </li>
            <li>
                <p>If an update is available, click "Restart to update" to install the update.</p>
            </li>
        </ol>
        <h2>Debugging - Incorrect Keyboard Language:</h2>
        <ol>
            <li>
                <p>Open the Chromebook settings by clicking on the clock in the bottom-right corner, then clicking on
                    the gear icon.</p>
            </li>
            <li>
                <p>In the settings menu, scroll down and click on "Advanced" to expand more options.</p>
            </li>
            <li>
                <p>Under the "Languages and input" section, click on "Language."</p>
            </li>
            <li>
                <p>Click on "Add languages" and select the correct language from the list. You can use the search bar to
                    find the language quickly.</p>
            </li>
            <li>
                <p>Once added, click on the three-dot menu next to the language and choose "Display Chrome OS in this
                    language."</p>
            </li>
            <li>
                <p>Restart your Chromebook to apply the changes.</p>
            </li>
        </ol>

        <h2>Debugging - Reverting to English Keyboard:</h2>
        <ol>
            <li>
                <p>Open the Chromebook settings by clicking on the clock in the bottom-right corner, then clicking on
                    the gear icon.</p>
            </li>
            <li>
                <p>In the settings menu, scroll down and click on "Advanced" to expand more options.</p>
            </li>
            <li>
                <p>Under the "Languages and input" section, click on "Language."</p>
            </li>
            <li>
                <p>Find the language you want to remove and click on the three-dot menu next to it.</p>
            </li>
            <li>
                <p>Choose "Remove" to revert back to English.</p>
            </li>
            <li>
                <p>To disable the onscreen keyboard, go to Accessibility settings and toggle off the onscreen keyboard
                    option.</p>
            </li>
        </ol>


        <h2>Debugging - Zoomed-In Display:</h2>
        <ol>
            <li>
                <p>Press the "Ctrl" and "-" keys simultaneously to zoom out the display. You can do this multiple times
                    until the display returns to the desired size.</p>
            </li>
            <li>
                <p>If the zoom level doesn't change, press the "Ctrl" and "0" keys simultaneously to reset the zoom
                    level to default.</p>
            </li>
            <li>
                <p>Adjust the display scaling settings by going to Chromebook settings > Displays > Display size. Drag
                    the slider to adjust the display scaling according to your preference.</p>
            </li>
        </ol>

        <h2>Common Chromebook Shortcuts/Accessibility Settings:</h2>
        <h3>Keyboard Shortcuts:</h3>
        <ul>
            <li>
                <p><strong>Switch Windows:</strong> Alt + Tab</p>
            </li>
            <li>
                <p><strong>Open Task Manager:</strong> Shift + Esc</p>
            </li>
            <li>
                <p><strong>Take a Screenshot:</strong> Ctrl + Show windows (Ctrl + F5)</p>
            </li>
            <li>
                <p><strong>Lock Screen:</strong> Search (or Launcher key) + L</p>
            </li>
            <li>
                <p><strong>Open Files App:</strong> Alt + Shift + M</p>
            </li>
            <li>
                <p><strong>Switch between Users:</strong> Ctrl + Alt + Shift + Back/Forward arrow</p>
            </li>
        </ul>

        <h3>Accessibility Settings:</h3>
        <ol>
            <li>
                <p>Open Chromebook settings.</p>
            </li>
            <li>
                <p>Scroll down and click on "Advanced."</p>
            </li>
            <li>
                <p>Under the "Accessibility" section, you'll find various options for customizing accessibility
                    settings:</p>
                <ul>
                    <li>
                        <p><strong>Display Size:</strong> Adjust the size of text and images on the screen.</p>
                    </li>
                    <li>
                        <p><strong>Screen Magnifier:</strong> Enlarge portions of the screen for better visibility.</p>
                    </li>
                    <li>
                        <p><strong>High Contrast Mode:</strong> Increase contrast for better readability.</p>
                    </li>
                    <li>
                        <p><strong>Keyboard Accessibility:</strong> Customize keyboard shortcuts and enable auto-click.
                        </p>
                    </li>
                    <li>
                        <p><strong>Text-to-Speech:</strong> Enable spoken feedback for on-screen text.</p>
                    </li>
                </ul>
            </li>
            <li>
                <p>Click on each option to customize settings according to your accessibility needs.</p>
            </li>
            <li>
                <p>Experiment with different settings to find the configuration that works best for you.</p>
            </li>
        </ol>

        <h3>Performing a Powerwash (Hard Reset):</h3>
        <p>If all else fails, you can perform a powerwash on your Chromebook to restore it to its factory settings:</p>
        <ol>
            <li>
                <p>Click on the clock in the bottom-right corner to open the system tray.</p>
            </li>
            <li>
                <p>Click on the gear icon to open "Settings."</p>
            </li>
            <li>
                <p>In the settings menu, scroll down and click on "Advanced" to expand more options.</p>
            </li>
            <li>
                <p>Under the "Reset settings" section, click on "Powerwash."</p>
            </li>
            <li>
                <p>Follow the on-screen instructions to complete the powerwash process.</p>
            </li>
        </ol>
        <p>If your Chromebook is locked and you're unable to access the settings, you can perform a powerwash using a
            keyboard shortcut:</p>
        <ol>
            <li>
                <p>Press and hold the "Ctrl" + "Alt" + "Shift" + "R" keys simultaneously.</p>
            </li>
            <li>
                <p>A pop-up window will appear asking if you want to reset your Chromebook. Click "Restart."</p>
            </li>
            <li>
                <p>Follow the on-screen instructions to complete the powerwash process. Note: You will have to either
                    use the GUSD_Guest or another network such a personal hotspot when going through the initial
                    onboarding. Once you connect to some network, it will begin Enterprise Enrollment and revert back to
                    the proper CBX subnetwork for MDM.</p>
            </li>
        </ol>
